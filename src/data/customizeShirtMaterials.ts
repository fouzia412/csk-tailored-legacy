export const useCustomizeShirtMaterials = () => {
  return useQuery({
    queryKey: ["customize-shirts"],
    queryFn: async (): Promise<MaterialItem[]> => {
      const { data } = await axios.get(
        `${import.meta.env.VITE_API_BASE_URL}/customize/shirts`,
      );
      return data?.customizes || [];
    },
  });
};
