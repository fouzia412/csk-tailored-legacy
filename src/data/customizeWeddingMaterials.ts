export const useCustomizeWeddingMaterials = () => {
  return useQuery({
    queryKey: ["customize-wedding"],
    queryFn: async (): Promise<MaterialItem[]> => {
      const { data } = await axios.get(
        `${import.meta.env.VITE_API_BASE_URL}/customize/wedding`,
      );
      return data?.customizes || [];
    },
  });
};
