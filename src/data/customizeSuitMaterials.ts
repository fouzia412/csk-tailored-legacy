export const useCustomizeSuitMaterials = () => {
  return useQuery({
    queryKey: ["customize-suits"],
    queryFn: async (): Promise<MaterialItem[]> => {
      const { data } = await axios.get(
        `${import.meta.env.VITE_API_BASE_URL}/customize/suits`,
      );
      return data?.customizes || [];
    },
  });
};
